# ----------------------------------------------------------------------
# 
# Dual attention -> 这次将gc模块加在卷积模块之间，然后将SE模块加在block最后
# 
# -----------------------------------------------------------------------

class DualBasicBlock(nn.Module):
    expansion = 1

    def __init__(self, inplanes, planes, stride=1, downsample=None, dual_att = True):
        super(DualBasicBlock, self).__init__()
        self.conv1 = conv3x3(inplanes, planes, stride)
        self.bn1 = nn.BatchNorm2d(planes, momentum=BN_MOMENTUM)
        self.relu = nn.ReLU(inplace=True)
        self.conv2 = conv3x3(planes, planes)
        self.bn2 = nn.BatchNorm2d(planes, momentum=BN_MOMENTUM)

        # 增加双重注意力
        self.dual_att = dual_att
        self.se = SE(planes)
        self.gc = ContextBlock(planes)
                
        self.downsample = downsample
        self.stride = stride


    def forward(self, x):
        residual = x

        out = self.conv1(x)
        out = self.bn1(out)
        out = self.relu(out)

        if self.dual_att:
            out = self.gc(out)

        out = self.conv2(out)
        out = self.bn2(out)

        if self.dual_att:
            out = self.se(out)

        if self.downsample is not None:
            residual = self.downsample(x)

        out = out + residual
        out = self.relu(out)

        return out



